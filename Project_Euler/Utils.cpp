#include "Utils.h"

int CharToInt(char theChar)
{
	if (theChar == '0')
		return 0;
	if (theChar == '1')
		return 1;
	if (theChar == '2')
		return 2;
	if (theChar == '3')
		return 3;
	if (theChar == '4')
		return 4;
	if (theChar == '5')
		return 5;
	if (theChar == '6')
		return 6;
	if (theChar == '7')
		return 7;
	if (theChar == '8')
		return 8;
	if (theChar == '9')
		return 9;
}